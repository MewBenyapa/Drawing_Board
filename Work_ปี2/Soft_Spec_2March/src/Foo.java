
public class Foo {

	private int a;
	private String b;
	private float c;
	private boolean d;
	
	public Foo() {
		a = 7;
		b = "Hello";
		c = 0.08f;
		d = false;
	}

	public int getA() {
		return a;
	}

	public String getB() {
		return b;
	}

	public float getC() {
		return c;
	}

	public boolean isD() {
		return d;
	}

	public void setA(int a) {
		this.a = a;
	}

	public void setB(String b) {
		this.b = b;
	}

	public void setC(float c) {
		this.c = c;
	}

	public void setD(boolean d) {
		this.d = d;
	}
	
}
